class Employee:
    def setid(self, id):
        self.id = id

    def getid(self):
        return self.id

    def setname(self, name):
        self.name = name

    def getname(self):
        return self.name

    def setdept(self, dept):
        self.dept = dept

    def getdept(self):
        return self.dept

    def setsalary(self, salary):
        self.salary = salary

    def getsalary(self):
        return self.salary
